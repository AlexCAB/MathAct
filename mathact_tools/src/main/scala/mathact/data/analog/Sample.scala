package mathact.data.analog

import mathact.data.Timed

/** Quantized representation of analog signal
  * Created by CAB on 19.11.2016.
  */

case class Sample(
  time: Long, // Virtual time in milliseconds
  shift: Double,
  value: Double)
extends Timed[Sample]{
  def time(t: Long): Sample = copy(time = t)
  override def toString = s"Sample(time = $time, shift = $shift, value = $value)"}
